namespace Myddleware\RegleBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * Config
 * @ORM\Table()
 * @ORM\Entity
 * @ORM\Table(indexes={@ORM\Index(name="Kconf_name", columns={"conf_name"})})
 */ 
class Config
{
    /**
     * @var integer
     *
     * @ORM\Column(name="conf_id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;
	
    /**
     * @var datetime
     *
	 * 
     * @ORM\Column(name="conf_name", type="string", length=100, nullable=false)
	 * 
     */
    private $name;


    /**
     * @var string
     *
	 * @ORM\Column(name="conf_value", type="string", length=255, nullable=false)
	 * 
     */
    private $value;


    /**
     * Get id
     *
     * @return integer 
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set name
     *
     * @param string $name
     * @return Config
     */
    public function setName($name)
    {
        $this->name = $name;
    
        return $this;
    }

    /**
     * Get name
     *
     * @return string 
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * Set value
     *
     * @param string $value
     * @return Config
     */
    public function setValue($value)
    {
        $this->value = $value;
    
        return $this;
    }

    /**
     * Get value
     *
     * @return string 
     */
    public function getValue()
    {
        return $this->value;
    }
}